export const t = derived(locale, ($locale) => (key: string) => {
    const keys = key.split('.');
    let result: any = translations[$locale];
    for (const k of keys) {
        if (result && result[k]) {
            result = result[k];
        } else {
            return key;
        }
    }
    return result;
});
